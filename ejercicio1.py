"""
Cree un programa que tome tres valores por consola multiplique el primero por el segundo 
y le sume el tercero. Presente el resultado en la terminal del editor
"""

x = int(input('Introduzca un valor: '))
y = int(input('Introduzca un valor: '))
z = int(input('Introduzca un valor: '))

resultado = ( x * y ) + z

print(f'La multiplicación del primer numero por el segundo mas el tercer número es {resultado}')

